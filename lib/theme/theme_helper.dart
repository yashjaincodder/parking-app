/// Class containing the supported color schemes.
class ColorSchemes {
  static final primaryColorScheme = ColorScheme.light(
    // Primary colors
    primary: Color(0XFFF5C116),
    primaryContainer: Color(0XFF2A364E),

    // Error colors
    errorContainer: Color(0XFFDB5149),
    onErrorContainer: Color(0XFF0F0F0F),

    // On colors(text colors)
    onPrimary: Color(0XFF371084),
    onPrimaryContainer: Color(0XFFFFFFFF),
  );
}

/// Class containing custom colors for a primary theme.
class PrimaryColors {
  // Black
  Color get black900 => Color(0XFF000000);

  // BlueGray
  Color get blueGray50 => Color(0XFFECF4F4);

  // Gray
  Color get gray200 => Color(0XFFEEECF0);

  // Orange
  Color get orangeA100 => Color(0XFFF5D774);

  // White
  Color get whiteA700 => Color(0XFFFFFDFD);
}

PrimaryColors get appTheme => ThemeHelper().themeColor();
ThemeData get theme => ThemeHelper().themeData();
