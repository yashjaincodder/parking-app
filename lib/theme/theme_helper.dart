          color: colorScheme.onErrorContainer,
          fontSize: 11.fSize,
          fontFamily: 'Kantumruy',
          fontWeight: FontWeight.w300,
        ),
        headlineSmall: TextStyle(
          color: appTheme.whiteA700,
          fontSize: 24.fSize,
